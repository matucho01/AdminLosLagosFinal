package ec.edu.epn.adminloslagos;

public class Inventario {
    private float galonesSuper;
    private float galonesExtra;
    private float galonesDiesel;
    
    public Inventario() {
        
    }

    public float getGalonesSuper() {
        return galonesSuper;
    }

    public void setGalonesSuper(float galonesSuper) {
        this.galonesSuper = galonesSuper;
    }

    public float getGalonesExtra() {
        return galonesExtra;
    }

    public void setGalonesExtra(float galonesExtra) {
        this.galonesExtra = galonesExtra;
    }

    public float getGalonesDiesel() {
        return galonesDiesel;
    }

    public void setGalonesDiesel(float galonesDiesel) {
        this.galonesDiesel = galonesDiesel;
    }
    
}
